let m,n;
m = prompt("Введіть число: ", 0);

if(m>0){
    n = 1;
    console.log("Ваше число: " + m + "\n" + "Результат: " + n);
} else if(m==0){
    n = 0;
    console.log("Ваше число: " + m + "\n" + "Результат: " + n);
} else{
    n = -1;
    console.log("Ваше число: " + m + "\n" + "Результат: " + n);
}
